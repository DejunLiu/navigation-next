#!/bin/sh

SRC_DIR=$1
COMPONENT=$2
OUTPUT_DIR=$3
CCC_ROOT=$4
BRANCH=$5

DEST_DIR=$OUTPUT_DIR/$COMPONENT
DEST_DIR_INCLUDE=$DEST_DIR/include
DEST_DIR_INCLUDE_IPHONE=$DEST_DIR/include/iphone
DEST_DIR_LIB=$DEST_DIR/lib
DEST_DIR_UNITEST=$DEST_DIR/unitest

mkdir $DEST_DIR
mkdir $DEST_DIR_INCLUDE
mkdir $DEST_DIR_INCLUDE_IPHONE
mkdir $DEST_DIR_LIB
mkdir $DEST_DIR_UNITEST

cp $CCC_ROOT/$COMPONENT/$BRANCH/include/* $DEST_DIR_INCLUDE
cp -rf $CCC_ROOT/$COMPONENT/$BRANCH/include/iphone $DEST_DIR_INCLUDE
cp $CCC_ROOT/$COMPONENT/$BRANCH/src/unittests/* $DEST_DIR_UNITEST
cp -rf $CCC_ROOT/$COMPONENT/$BRANCH/src/unittests/iphone $DEST_DIR_UNITEST
cp $SRC_DIR/lib/lib$COMPONENT.a $DEST_DIR_LIB
