static void TestNetworkQueryResponse(void)
{
	CSL_Network* network = NULL;
    NB_NetworkQuery request = { 0 };
    NB_Error err = NE_OK;
//    char *pToken = "v4+62AWyISFIbRHJzEhoN7ro6alRlM+lKRkW2nAJ";  // dev3 - bluebox/http mux testing
	PAL_Instance* pal = PAL_CreateInstance();
	NB_NetworkConfiguration netconf = {{ 0 }};
	nsl_strcpy(netconf.hostname, "ef5752b545374ef8f800bca92b3d259144d6dc2e.navbuilder.nimlbs.net");
	netconf.iden         = TestNetworkCreateIden();
	netconf.port         = 8128;
	netconf.protocol     = NB_NetworkProtocol_TCP;
	netconf.tpslibData   = (char*)STATIC_TPS_LIB;
	netconf.tpslibLength = sizeof(STATIC_TPS_LIB);

    CU_ASSERT_EQUAL(PAL_NetInitialize(pal), PAL_Ok);

	err = CSL_NetworkCreate(pal, &netconf, &network);
    CU_ASSERT_EQUAL(err, NE_OK);
	CU_ASSERT_PTR_NOT_NULL(network);

    // Query the network
    request.action = TestNetworkMapNetCB;
    request.progress = TestNetworkMapNetCBProgress;
    request.query = TestNetworkCreateMapTPS();
    nsl_strcpy(request.target, "map,dds");
    request.targetlen = -1;
    request.qflags = 1;
    request.failFlags = 3;
    request.list.stqe_next = NULL;
    request.expecting = 0;
    request.sentid = 0;
    request.packetlen = 0;
    request.pendingdata = 0;

    g_mapCallbackCompletedEvent = CreateCallbackCompletedEvent();
    CU_ASSERT_PTR_NOT_NULL(g_mapCallbackCompletedEvent);

    CSL_NetworkRequest(network, &request);  

    CU_ASSERT(WaitForCallbackCompletedEvent(g_mapCallbackCompletedEvent, 60000));

    DestroyCallbackCompletedEvent(g_mapCallbackCompletedEvent);

    err = CSL_NetworkDestroy(network);
    CU_ASSERT_EQUAL(err, NE_OK);

    te_dealloc(netconf.iden);

	PAL_Destroy(pal);
}

